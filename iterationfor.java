 package mvpcorejava;

public class iterationfor {

	public static void main(String[] args) {
  //increment
for ( int a =1; a<=10; a++){
	System.out.println(a);
}
 //decrement
for ( int b = 10; b>=1; b--) {
	System.out.println(b);
}
for (int c=1,d=10; c<=d; c++,d--) {
	System.out.println(c+" "+d);
}
	}
}
